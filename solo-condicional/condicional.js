let diaSemana = parseInt(prompt("Escribe un numero del 1 al 7 y te dara un dia de la semana"))

switch (diaSemana) {
    case 1:
        alert("el dia es domingo")
        break
    case 2:
        alert("el dia es lunes")
        break
    case 3:
        alert("el dia es martes")
        break
    case 4:
        alert("el dia es miercoles")
        break
    case 5:
        alert("el dia es jueves")
        break
    case 6:
        alert("el dia es viernes")
        break
    case 7:
        alert("el dia es sabado")
        break
    default:
        alert("Error")
}